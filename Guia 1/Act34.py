#Leer un número comprendido entre uno y siete, ambos inclusive e imprimir el nombre del
#día de la semana Correspondiente.

numero=int(input("Ingrese un numero del 1 al 7: "))

if numero==1:
    print("Es lunes")
elif numero==2:
    print("Es Martes")
elif numero==3:
    print("Es Miercoles")
elif numero==4:
    print("Es jueves")
elif numero==5:
    print("Es viernes")
else:
    print("Error, es un numero del 1 al 7 :D")

    